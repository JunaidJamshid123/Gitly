package com.example.gitly.presentation.base

/**
 * Base interface for all UI states in the application.
 * All screen UI states should implement this interface.
 */
interface UiState

/**
 * Base interface for all UI events in the application.
 * Events are one-time actions like showing a snackbar, navigating, etc.
 */
interface UiEvent

/**
 * Base interface for all user intents/actions.
 * Represents actions that the user can perform.
 */
interface UiAction
